# A tuple is a type of data structure.
# They are inmutable. So they cannot be modified once created.
# python will display an error if this is attempted.
# But their entried can be accessed easily, similarly to a list.
coordinates = (4, 5)
print(coordinates)
print(coordinates[0])
print(coordinates[1])

# Tuples can be nested in a list
coordinates_list = [(4, 5), (6, 7), (80, 34)]
print(coordinates_list)
print(coordinates_list[1])
