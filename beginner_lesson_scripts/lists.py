# [] is used to create a list
friends = ["Kevin", "Karen", "Jim", "Oscar", "Toby"]

# List elements cna be acessed by their indexing positions
print(friends[0])
print(friends[1])

# This can even be done backwards
print(friends[-1])
print(friends[-2])

# A range of elements is also possible
print(friends[1:])
print(friends[1:4])

# We can modify values of elements usign their indexing positions
friends[1] = "Mike"
print(friends[1])
